CREATE TABLE student (
    id SERIAL PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    birth_date DATE NOT NULL,
    gender INTEGER NOT NULL,
    class_room_id INTEGER REFERENCES class_room (id)
);

-- テーブルとカラムにコメントを追加
COMMENT ON TABLE student IS '保育園の園児情報を管理するテーブル';
COMMENT ON COLUMN student.id IS '園児ID（主キー）';
COMMENT ON COLUMN student.name IS '園児名';
COMMENT ON COLUMN student.birth_date IS '生年月日';
COMMENT ON COLUMN student.gender IS '性別（1=男、2=女）';
COMMENT ON COLUMN student.class_room_id IS '所属クラスID（外部キー）';

